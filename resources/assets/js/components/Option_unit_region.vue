<template>
    <div class="container">
        <div class="row">
            <div class="col-md-12 text-right">
                <a href="http://pm.vkeepers/options/">
                    <span class="glyphicon glyphicon-menu-left btn-lg btn-back" aria-hidden="true"></span>    
                </a>
            </div>
            <div class="col-md-6 col-md-offset-3">
                <div class="panel panel-default">
                    <div class="panel-heading">Edit list: <strong>REGION Unit</strong></div>
                    <div class="panel-body">
    <!-- add new unit -->
                        <form>
                            <div class="form-group">
                                <label for="email">Add new region</label>
                                <input type="text" class="form-control" v-model="selected_add.add" placeholder="Typing new subbrand name ...">
                            </div>
                            <div class="form-group text-center">
                                <button class="btn btn-success btn-lg" @click.prevent="add">Add
                                    <span class="glyphicon glyphicon-record" aria-hidden="true"></span>
                                </button>
                            </div>
                        </form>
                    </div>
    <!-- edit current unit -->
                    <div class="panel-body">
                        <form>
                            <div class="form-group">
                                <label for="region">Select region</label>
                                <select name="region" class="form-control" v-model="selected_update.up_at">
                                    <option selected disabled value="">Select region ...</option>
                                    <option v-for="(region, key) in regions" :value="region.id">{{ region.name }}</option>
                                </select>
                            </div>
                            <div class="form-group">
                                <div>
                                    <label for="email">Update at</label>
                                    <input type="text" class="form-control" v-model="selected_update.up_to" placeholder="Typing updated name ...">
                                </div>
                            </div>
                            <div class="form-group text-center">
                                <button class="btn btn-warning btn-lg" @click.prevent="update">Update
                                    <span class="glyphicon glyphicon-wrench" aria-hidden="true"></span>
                                </button>
                            </div>
                        </form>
                    </div>
    <!-- delete current unit -->
                    <div class="panel-body">
                        <form>
                            <div class="form-group">
                                <label for="brand">Select region</label>
                                <select name="brand" class="form-control" v-model="selected_delete.delete">
                                    <option selected disabled value="">Select region ...</option>
                                    <option v-for="(region, key) in regions" :value="region.id">{{ region.name }}</option>
                                </select>
                            </div>
                            <div class="form-group text-center">
                                <button class="btn btn-danger btn-lg" @click.prevent="del">Delete
                                    <span class="glyphicon glyphicon-trash" aria-hidden="true"></span>
                                </button>
                            </div>
                        </form>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>

import swal from 'sweetalert2'
import axios from 'axios'

export default {
    data(){
        return{
          regions: "",
          selected_add:{
              add: ""
          },
          selected_update:{
              up_to: "",
              up_at: ""

          },
          selected_delete:{
              delete: ""
          }
        }
    },
    methods:{
        getRegion(){
            axios.get('/getData').then((response)=>{
                this.regions = response.data
            })
        },
        add(){
            axios.post('/regionAddData', this.selected_add).then((response)=>{
                if(response.data = '1'){
                    swal('Created', 'New region unit created', 'success').then((response)=>{
                        window.location.href = "/options"
                    })
                }
            })
        },
        update(){
            axios.post('/regionUpdateData', this.selected_update).then((response)=>{
                if(response.data = '1'){
                    swal('Updated', 'Region unit updated', 'success').then((response)=>{
                        window.location.href = "/options"
                    })
                }
            })
        },
        del(){
            axios.post('/regionDeleteData', this.selected_delete).then((response)=>{
                if(response.data = '1'){
                    swal('Deleted', 'Region unit deleted', 'success').then((response)=>{
                        window.location.href = "/options"
                    })
                }
            })
        }
    },
    mounted(){
        this.getRegion()
    }
}
</script>

